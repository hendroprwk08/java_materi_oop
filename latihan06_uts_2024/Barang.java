package latihan06_uts_2024;

public class Barang {
    private String kode, nama;

    public Barang(String kode, String nama) {
        this.kode = kode;
        this.nama = nama;
    }

    public String getKode() {
        return kode;
    }

    public String getNama() {
        return nama;
    }

}
